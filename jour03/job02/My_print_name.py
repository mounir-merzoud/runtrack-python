def My_print_name(name):
    if name == "mounir":
        print("Mon nom c'est Mounir.")
    elif name == "jhon":
        print("Mon nom c'est Jhon.")
    elif name == "malick":
        print("Mon nom c'est Malick.")
    else:
        return

